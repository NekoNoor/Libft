#include "libft.h"

void		*ft_memchr(const void *s, int c, size_t n)
{
	unsigned const char	*s_c;

	s_c = s;
	while (*s_c != (unsigned char)c && n)
	{
		s_c++;
		n--;
	}
	if (n)
		return ((void *)s_c);
	return (NULL);
}
